package com.learningdebunked.mock.dto;

/**
 * DTO to capture the headers
 */
public class Header<K,V> {

    private K key;
    private V value;

    public K getKey() {
        return key;
    }

    public void setKey(K key) {
        this.key = key;
    }

    public V getValue() {
        return value;
    }

    public void setValue(V value) {
        this.value = value;
    }
}
